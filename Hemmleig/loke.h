
#pragma once

unsigned char lokedata[32] = {0x12,0x50,0x14,0x1F,0x2D,0x2A,0x02,0x13,0x11,0x03,0x5F,0x50,0x36,0x2A,0x16,0x12,0x27,0x11,0x0D,0x07,0x2F,0x24,0x2A,0x07,0x1E,0x29,0x2C,0x31,0x08,0x2A,0x23,0x11};

void loke(unsigned char* input,int len,int mod) {

	for (int i = 0 ; i < len; i++) {

		input[i] ^= lokedata[i % mod];

	}

}
